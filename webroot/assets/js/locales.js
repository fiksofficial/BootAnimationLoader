function detectLang() {
  const saved = localStorage.getItem('bal_lang');
  if (saved && LOCALES[saved]) return saved;
  const nav = navigator.language || navigator.userLanguage || 'en';
  const short = nav.split('-')[0].toLowerCase();
  if (LOCALES[short]) return short;
  if (nav.startsWith('zh')) return 'zh';
  if (nav.startsWith('ja')) return 'ja';
  if (nav.startsWith('ko')) return 'ko';
  localStorage.setItem('bal_lang', 'en');
  return 'en';
}

let currentLang = detectLang();

function setLang(lang) {
  if (LOCALES[lang]) {
    currentLang = lang;
    localStorage.setItem('bal_lang', lang);
    document.documentElement.lang = lang;
    const sel = document.getElementById('lang-select');
    if (sel) sel.value = lang;
    applyTranslations();
  }
}

function t(key) {
  return LOCALES[currentLang][key] || LOCALES.en[key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (LOCALES[currentLang][key] !== undefined) {
      el.textContent = LOCALES[currentLang][key];
    }
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const pairs = el.getAttribute('data-i18n-attr').split(';');
    pairs.forEach(pair => {
      const [attr, key] = pair.split(':');
      if (attr && key && LOCALES[currentLang][key] !== undefined) {
        el.setAttribute(attr.trim(), LOCALES[currentLang][key]);
      }
    });
  });
}
